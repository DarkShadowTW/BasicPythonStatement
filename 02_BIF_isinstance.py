#BIF : Built-in Functions

#Check variant is a list
x = [1, 2, 3]
if isinstance(x, list):
    print("x is a list")
else:
    print("x is not a list")

y = 3
if isinstance(y, list):
    print("y is a list")
else:
    print("y is not a list")

#Check variant is a integer
y = 3
if isinstance(y, int):
    print("y is a int")
else:
    print("y is not a int")